package csmeditor;

public class GeneralReportInfo {

	private String name;
	private String afm;
	private double totalSales;
	private double tousersSales;
	private double skirtsSales;
	private double shirtsSales;
	private double coatsSales;
	private double commissionSales;

	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getAfm() {
		return afm;
	}
	
	public void setAfm(String afm) {
		this.afm = afm;
	}
	
	public double getTotalSales() {
		return totalSales;
	}
	
	public void setTotalSales(double totalSales) {
		this.totalSales = totalSales;
	}
	
	public double getSkirtsSales() {
		return skirtsSales;
	}
	
	public void setSkirtsSales(double skirtsSales) {
		this.skirtsSales = skirtsSales;
	}
	
	public double getTousersSales() {
		return tousersSales;
	}
	
	public void setTousersSales(double tousersSales) {
		this.tousersSales = tousersSales;
	}
	
	public double getShirtsSales() {
		return shirtsSales;
	}
	
	public void setShirtsSales(double shirtsSales) {
		this.shirtsSales = shirtsSales;
	}
	
	public double getCoatsSales() {
		return coatsSales;
	}
	
	public void setCoatsSales(double coatsSales) {
		this.coatsSales = coatsSales;
	}
	
	public double getCommissionSales() {
		return commissionSales;
	}
	
	public void setCommissionSales(double commissionSales) {
		this.commissionSales = commissionSales;
	}
}
